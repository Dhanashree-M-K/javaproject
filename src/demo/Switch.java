package demo;

public class Switch {

	
	public static void main(String[] args) {
				char choice = 'B';
				switch (choice)
				{
				case 'A' : System.out.println("Excellent");	
		            break;
				case 'B' : System.out.println("Good");
		    		break;
				case 'C' : System.out.println("Average");
		            break;
				case 'D' : System.out.println("Fail");
		            break;
		 default:
		          System.out.println("Invalid choice");
				}
			
			}

		

	}
	


